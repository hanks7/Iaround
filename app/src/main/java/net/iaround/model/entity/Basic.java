
package net.iaround.model.entity;

public class Basic
{
	private int beginage;// 开始年龄
	private int endage;// 结束年龄
	private String thinktext;
	private int withwho;
	
	public int getBeginage( )
	{
		return beginage;
	}
	
	public void setBeginage( int beginage )
	{
		this.beginage = beginage;
	}
	
	public int getEndage( )
	{
		return endage;
	}
	
	public void setEndage( int endage )
	{
		this.endage = endage;
	}
	
	public String getThinktext( )
	{
		return thinktext;
	}
	
	public void setThinktext( String thinktext )
	{
		this.thinktext = thinktext;
	}
	
	public int getWithwho( )
	{
		return withwho;
	}
	
	public void setWithwho( int withwho )
	{
		this.withwho = withwho;
	}
	
	
}
